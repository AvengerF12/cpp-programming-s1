// The program takes two input numbers and sums them

#include <iostream>

using namespace std;

int main1()
{
	int n1, n2 = 0;

	cout << "Give me the first number: " << endl;

	cin >> n1;

	cout << "Give me the second number: " << endl;

	cin >> n2;

	cout << "The sum is equal to: " << n1 + n2 << endl;

	cout << "The product is equal to: " << n1 * n2 << endl;

	return 0;
}